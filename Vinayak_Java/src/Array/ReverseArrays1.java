package Array;

public class ReverseArrays1 {
	
	public static void main(String[] args) {
		
	
	
	int a[]= {10,20,40,60,50,30};
	   
	
	for (int i=0     ;i<a.length;    i++) {     
		 
		
		
		System.out.print(a[i]+" ");
		
	}
	
	System.out.println();
	
	     
	for (int k=a.length-1;  k>=0;  k--) {
		
		System.out.print(a[k]+" ");
	}
	

}

	
}