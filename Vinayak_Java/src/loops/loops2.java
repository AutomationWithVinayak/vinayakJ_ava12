
package loops;

public class loops2 {

	public static void main(String[] args) {
		
		for(int k=1; k<=20; k=k+2) {   
			System.out.println(k);
		}
		
		
		System.out.println();
		for(int a=1; a<=20; a=a+2) {
			
			System.out.print(" "+a);
		}
	}
}

 

