package loops;

public class loops3 {
	
	public static void main(String[] args) { 
		
		for(int e=2; e<=20; e=e+2) {
			
			System.out.println(e);
		}
		
		System.out.println();
		for(int e=20; e>=2; e=e-2) {
			System.out.println(e);
		}
		
		System.out.println();
		for(int e=2; e<=20; e=e+2) {
			System.out.print(" "+e);
			
		}
		System.out.println();
		
		for(int e=20; e>=2; e=e-2) {
			System.out.print(" "+e);
			
		}
		
	
	}

}
