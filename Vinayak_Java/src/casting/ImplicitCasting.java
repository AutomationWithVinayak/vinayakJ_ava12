package casting;

public class ImplicitCasting {
	
	public static void main(String[] args) {
		
		int a=5;
		System.out.println(a);
		
		double b=a;                    //primetive :- convert one data into another data type
		
		                                               //2 non prime...
		                              // pri--- implicit casting 2.explicit casting
		                               
		                     /* Converting lower data type into higher data type is called implicit casting
		                        It aslo called as winding casting where memory size goes incresing */
		
		
		System.out.println(b);
		
	}

}
