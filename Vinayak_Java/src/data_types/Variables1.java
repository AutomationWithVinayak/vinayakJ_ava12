package data_types;

public class Variables1 {
 public static void main(String[] args) {
	 
	 //1.variable declaration
	 
	 String Studantname;
	 String Lastname;
	 int Studantrollno;
	 String Studantmailid;
	 char Studantgrade;
	 float Studantpercentage;
	 
	 
	 //2.variable initialization
	 
	 Studantname ="Rahul";
	 Lastname ="Patil";
	 Studantrollno =15;
	 Studantmailid ="rahulpatil@gmail.com";
	 Studantgrade ='A';
	 Studantpercentage =65.35f;
	 
	 
	 //3.Usage
	 
	 System.out.println("Name = " + Studantname + Lastname);
	 System.out.println("Rollno = " + Studantrollno);
	 System.out.println("Mail Id = " + Studantmailid);
	 System.out.println("Grade = " + Studantgrade);
	 System.out.println("Percentage = " + Studantpercentage + "%"); 
	 	 
	 
 }

}
